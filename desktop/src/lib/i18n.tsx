let currentLanguage: AppLanguage = DEFAULT_LANGUAGE;

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>(currentLanguage);

  useEffect(() => {
    let cancelled = false;
    void getPref<AppLanguage>(APP_LANGUAGE_PREF_KEY)
      .then((saved) => {
        if (cancelled || !isAppLanguage(saved)) return;
        currentLanguage = saved;
        setLanguageState(saved);
      })
      .catch((err) => {
        console.warn("[i18n] failed to load language preference", err);
      });
    return () => {
      cancelled = true;
    };
  }, []);

  const setLanguageValue = useCallback((next: AppLanguage) => {
    currentLanguage = next;
    setLanguageState(next);
    void setPref(APP_LANGUAGE_PREF_KEY, next).catch((err) => {
      console.warn("[i18n] failed to persist language preference", err);
    });
  }, []);

  const tValue = useCallback(
    (key: string, params?: TranslationParams) => translate(key, params, language),
    [language],
  );

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      setLanguage: setLanguageValue,
      t: tValue,
    }),
    [language, setLanguageValue, tValue],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider");
  return value;
}

export function getAppLanguage(): AppLanguage {
  return currentLanguage;
}

export function translate(
  key: string,
  params?: TranslationParams,
  language: AppLanguage = currentLanguage,
): string {
  const template =
    TRANSLATIONS[language][key] ??
    (language === "zh" ? TRANSLATIONS.en[key] : undefined) ??
    key;
  return formatTranslation(template, params);
}

function formatTranslation(template: string, params?: TranslationParams): string {
  if (!params) return template;
  return template.replace(/\{([A-Za-z0-9_]+)\}/g, (match, name) => {
    const value = params[name];
    return value === null || value === undefined ? match : String(value);
  });
}

function isAppLanguage(value: unknown): value is AppLanguage {
  return value === "zh" || value === "en";
}
